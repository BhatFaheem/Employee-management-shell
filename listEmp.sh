Employee_File="$1"

if [[ ! -s "$Employee_File" ]];
    then 
        echo "No Employees found "
        exit 1
fi

echo "Listing all User's..."

echo "=========================================================================================="
echo "Emp ID  | Name                      | Dept | Salary     | Location        | Hire Date"
echo "=========================================================================================="

awk -F: '{printf "%-7s | %-25s | %-4s | %-10s | %-15s | %s\n", $1, $2, $3, $4, $5, $6}' "$Employee_File"

echo "=========================================================================================="